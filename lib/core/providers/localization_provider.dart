  LocalizationNotifier() : super(LocalizationState());

  void toggleLanguage() {
    state = state.copyWith(
      language: state.language == AppLanguage.fr ? AppLanguage.wo : AppLanguage.fr,
    );
  }

  void setLanguage(AppLanguage lang) {
